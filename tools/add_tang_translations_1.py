with open('data/builtin/chinese/tangpoems_full.json', encoding='utf-8') as f:
    data = json.load(f)

added = 0
for text in data['texts']:
    tid = text['id']
    if tid in TRANSLATIONS:
        info = TRANSLATIONS[tid]
        if not text.get('translation') and info.get('translation'):
            text['translation'] = info['translation']
            added += 1
        if not text.get('word_notes') and info.get('word_notes'):
            text['word_notes'] = info['word_notes']

with open('data/builtin/chinese/tangpoems_full.json', 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)

has_t = sum(1 for t in data['texts'] if t.get('translation'))
print(f'Added translations to {added} poems. Now {has_t}/{len(data["texts"])} have translation.')
