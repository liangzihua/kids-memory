# Load the file
with open('data/builtin/chinese/primary/primary_poems_full.json', encoding='utf-8') as f:
    data = json.load(f)

# Add translation and word_notes to each poem
for text in data['texts']:
    poem_id = text['id']
    if poem_id in POEMS_DATA:
        extra = POEMS_DATA[poem_id]
        if 'translation' not in text:
            text['translation'] = extra['translation']
        if 'word_notes' not in text and 'word_notes' in extra:
            text['word_notes'] = extra['word_notes']

# Save
with open('data/builtin/chinese/primary/primary_poems_full.json', 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)

